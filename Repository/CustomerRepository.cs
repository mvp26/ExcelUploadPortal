using ExcelUploadPortal.Data;
using ExcelUploadPortal.Models;
using Microsoft.EntityFrameworkCore;

namespace ExcelUploadPortal.Repository
{
    public class CustomerRepository : ICustomerRepository
    {
        private readonly ExcelUploadPortalContext _context;

        public CustomerRepository(ExcelUploadPortalContext context)
        {
            _context = context;
        }

        public async Task<List<Customer>> GetAllCustomersAsync()
        {
            return await _context.Customers.ToListAsync();
        }

        public async Task<Customer> GetCustomerByIdAsync(int id)
        {
            return await _context.Customers.FindAsync(id);
        }

        public async Task AddCustomerAsync(Customer customer)
        {
            await _context.Customers.AddAsync(customer);
            await _context.SaveChangesAsync();
        }

        public async Task UpdateCustomerAsync(Customer customer)
        {
            _context.Customers.Update(customer);
            await _context.SaveChangesAsync();
        }

        public async Task DeleteCustomerAsync(Customer customer)
        {
            _context.Customers.Remove(customer);
            await _context.SaveChangesAsync();
        }
    }

}
